// Variables Declaration According To Data Types

// Number Data Type : Accept Both Integer And Parse(Decimal)
let a:number = 10;
console.log(a)

// String Data Type
let b:string = "TypeScript"
console.log(b)

// Boolean Data Type
let c:boolean = true
console.log(c)

// Number Here Accepting Parse(Decimal)
let d:number = 10.11;
console.log(d)

// Any Data Type
let e:any = "10 Aneeq";
console.log(e)