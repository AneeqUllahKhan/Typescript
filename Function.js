// Function Type
function calc() {
    return 12 + 12;
}
console.log(calc());
// Function And Parameter Type
function calce(e) {
    return e;
}
console.log(calce(10));
console.log(calc());
// Function With Two Parameter Type
function ca(e, d) {
    return d ? e + d : e;
}
console.log(ca(10, 20));
// Function With String Type 
function cal() {
    return "Hello";
}
console.log(cal());
